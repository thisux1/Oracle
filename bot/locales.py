def t(key: str, **kwargs) -> str:
    """Translate a key to the currently configured language.

    Reads config.language at call time, so it reflects live config changes.
    Falls back to Portuguese if the key or language is missing.
    """
    lang = getattr(config, "language", "pt")
    translations = TRANSLATIONS.get(lang) or TRANSLATIONS["pt"]
    template = translations.get(key) or TRANSLATIONS["pt"].get(key) or key
    try:
        return template.format(**kwargs)
    except (KeyError, ValueError):
        return template


def set_language(lang: str) -> None:
    """Set bot language at runtime and persist to options.ini."""
    lang = lang.lower().strip()
    if lang not in ("pt", "en"):
        return
    config.language = lang
    config.userOptions["language"] = lang
    try:
        import options_resolver
        options_resolver.editData("language", lang)
    except Exception:
        pass
